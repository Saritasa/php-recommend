<script>
import marked from 'marked';

export default {
  name:    'TransformMixin',
  methods: {
    /**
     * Parse text to markdown format.
     *
     * @param {string|null} text - Text to parse.
     *
     * @return {string|null}
     */
    markdown(text) {
      if (text) {
        return marked(text);
      }

      return text;
    },
  },
};
</script>
